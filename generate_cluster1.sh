#!/bin/sh

GOTKSYNC="./clusters/cluster1/flux-system/gotk-sync.yaml"

echo "# This manifest was generated by flux. DO NOT EDIT." > $GOTKSYNC

flux create source git k8s-gitops \
    --url=https://github.com/dcatwoohoo/k8s-gitops \
    --branch=main \
    --interval=3m \
    --export >> $GOTKSYNC

flux create kustomization k8s-gitops \
    --source=k8s-gitops \
    --path="./clusters/cluster1/" \
    --prune=true \
    --interval=5m \
    --export >> $GOTKSYNC
